void USART1_SendString_DMA(const char *str) {
    uint16_t length = 0;

    // Tính độ dài chuỗi
    while (str[length] != '\0') {
        length++;
    }

    // Đảm bảo DMA không đang hoạt động
    while (LL_DMA_IsEnabledStream(DMA2, LL_DMA_STREAM_7));

    // Cấu hình DMA để truyền chuỗi
    LL_DMA_SetMemoryAddress(DMA2, LL_DMA_STREAM_7, (uint32_t)str);
    LL_DMA_SetDataLength(DMA2, LL_DMA_STREAM_7, length);

    // Bật DMA Stream
    LL_DMA_EnableStream(DMA2, LL_DMA_STREAM_7);
}
